package net.openchrom.xxd.process.supplier.templates.ui.wizards;

import org.eclipse.jface.wizard.WizardPage;
import org.eclipse.swt.SWT;
import org.eclipse.swt.layout.GridLayout;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.swt.widgets.Label;

public class PeaksPage extends WizardPage {

	public PeaksPage(String pageName) {
		super(pageName);
		setTitle("Modify/Add Peaks");
		setDescription("Manual peak modifier/detector");
		setErrorMessage(null);
	}

	@Override
	public void createControl(Composite parent) {

		Composite composite = new Composite(parent, SWT.NONE);
		composite.setLayout(new GridLayout(1, false));
		//
		Label label = new Label(composite, SWT.NONE);
		label.setText("Manual peak detector UI ... in progress.");
		//
		setControl(composite);
	}
}
